	<h1>À propos</h1>
    <hr class="my-4">
    <p>Cette plateforme a été réalisée dans le cadre d'un projet de sécurité de 4<sup>ème</sup> année à l'<a href="https://www.insa-centrevaldeloire.fr/fr/">INSA CVL</a>. 
    Ce projet a été encadré par le professeur Benjamin Nguyen.</p>
    <p>Elle a pour but d'être le support d'une compétition d'anonymisation de données tabulaires de géolocalisation et d'horodatage.</p>
    <p>Cette compétition se déroule en deux phases. Au cours de la première phase, les différentes équipes tenteront d'anonymiser de leur mieux les données fournies. 
    Puis, lors de la seconde phase, les équipes essayeront de réidentifier un maximum de données anonymisées par les autres équipes au cours de la première phase.</p>
    <p>Chaque équipe obtiendra ainsi un score d'attaque suite à la réidentification et on déduira des scores d'attaques des différentes équipes un score de défense propre 
    à chaque jeu de données anonymisés ; ce qui nous permettra d'établir un classement.</p>
